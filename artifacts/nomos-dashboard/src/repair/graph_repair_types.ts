/**
 * graph_repair_types.ts
 *
 * Canonical types for graph-native repair planning, execution, and validation.
 *
 * A repair is a sequence of explicit graph transformations — not prose.
 * Every action identifies a target node or edge by ID, carries a typed payload,
 * and includes a human-readable rationale.
 *
 * Design principles:
 *   - Explicit: every action names what it changes (node ID, edge ID)
 *   - Local: actions are minimal — one structural change per action
 *   - Auditable: rationale is always present
 *   - Replayable: applying actions in order deterministically produces the repaired graph
 *   - Validated: after application, feasibility is re-checked against the constraint set
 *
 * Minimality order (from least to most invasive):
 *   1. UPDATE_QUANTITY       — change a measure amount on an existing node
 *   2. UPDATE_RELATION_OFFSET — change a temporal offset on an existing edge
 *   3. ADD_ENTITY             — create a new node (with measures + relation)
 *   4. REMOVE_ENTITY          — delete an existing node
 *   5. ADD_RELATION           — create a new edge
 *   6. REMOVE_RELATION        — delete an existing edge
 *   7. MOVE_ENTITY_TO_CANDIDATE — change which candidate owns an entity
 *   8. UPDATE_UNIT            — change the unit on an existing measure
 *   9. ADD_TAG / REMOVE_TAG   — change classification (only if policy allows)
 */

/* =========================================================
   Action types
   ========================================================= */

/**
 * The ten canonical repair action types.
 *
 * Each type maps to a specific structural mutation of the CanonicalGraph.
 */
export type GraphRepairActionType =
  | "ADD_ENTITY"
  | "REMOVE_ENTITY"
  | "UPDATE_QUANTITY"
  | "UPDATE_UNIT"
  | "ADD_RELATION"
  | "REMOVE_RELATION"
  | "UPDATE_RELATION_OFFSET"
  | "MOVE_ENTITY_TO_CANDIDATE"
  | "ADD_TAG"
  | "REMOVE_TAG";

/* =========================================================
   Action
   ========================================================= */

/**
 * A single, atomic graph mutation.
 *
 * id: stable identifier, e.g. "ra_0", "ra_1"
 * type: what kind of structural change this is
 * targetNodeId: the node being modified (null for ADD_ENTITY / ADD_RELATION)
 * targetEdgeId: the edge being modified (null for most node-level actions)
 * payload: action-specific parameters (see payload contracts below)
 * rationale: why this action was generated (always non-empty)
 *
 * Payload contracts by type:
 *   ADD_ENTITY:
 *     { label, tags, measures: [{ amount, unit, unitNormalized, dimension }],
 *       kind: "entity"|"quantity", candidateId?, anchorLabel?, relation?, offsetAmount?, offsetUnit? }
 *
 *   REMOVE_ENTITY:
 *     { label }  (informational — executor uses targetNodeId)
 *
 *   UPDATE_QUANTITY:
 *     { unit, newAmount, previousAmount }
 *
 *   UPDATE_UNIT:
 *     { previousUnit, newUnit, amount }
 *
 *   ADD_RELATION:
 *     { fromNodeId, toNodeId, kind, offset?: { amount, unit }, confidence? }
 *
 *   REMOVE_RELATION:
 *     { kind }  (informational — executor uses targetEdgeId)
 *
 *   UPDATE_RELATION_OFFSET:
 *     { previousOffsetAmount, newOffsetAmount, unit }
 *
 *   MOVE_ENTITY_TO_CANDIDATE:
 *     { fromCandidateId, toCandidateId }
 *
 *   ADD_TAG:
 *     { tag }
 *
 *   REMOVE_TAG:
 *     { tag }
 */
export interface GraphRepairAction {
  /** Stable identifier within this plan, e.g. "ra_0". */
  id: string;

  /** What structural change this action makes. */
  type: GraphRepairActionType;

  /**
   * Graph node ID targeted by this action.
   * Null for ADD_ENTITY (node doesn't exist yet).
   * Null for ADD_RELATION (edge-only action).
   */
  targetNodeId?: string | null;

  /**
   * Graph edge ID targeted by this action.
   * Null for node-level actions (ADD_ENTITY, UPDATE_QUANTITY, etc.).
   */
  targetEdgeId?: string | null;

  /** Action-specific parameters. See payload contracts above. */
  payload: Record<string, unknown>;

  /**
   * Human-readable explanation of why this action was chosen.
   * Example: "increase cyclic dextrin from 40g to 60g to meet ≥60g fast-carb threshold"
   */
  rationale: string;
}

/* =========================================================
   Repair plan
   ========================================================= */

/**
 * A complete, ordered sequence of actions that should restore feasibility
 * for a single violated constraint.
 *
 * Plans are generated by buildGraphRepairPlan() and applied by applyGraphRepairPlan().
 * After application, validateGraphRepairPlan() re-checks feasibility.
 */
export interface GraphRepairPlan {
  /** Stable plan identifier, e.g. "plan_c_carb_timing". */
  id: string;

  /** The constraintId that triggered this plan. */
  constraintId: string;

  /**
   * Human-readable name of the target variable or entity.
   * Example: "fast carbs before lifting", "oats quantity"
   */
  variableName: string;

  /**
   * Ordered list of actions. Apply in index order.
   * Actions may depend on prior actions (e.g. ADD_ENTITY followed by ADD_RELATION).
   */
  actions: GraphRepairAction[];

  /**
   * Predicted effects after all actions are applied.
   * Example:
   *   "fast carb sum will increase from 40g to 60g"
   *   "qualifying entities within 90-min window: 2"
   */
  expectedRepairEffect: string[];

  /**
   * 0.0–1.0 score where 1.0 = perfectly minimal (single field change).
   * Decreases with invasiveness: update quantity (0.9) > update offset (0.8) >
   * add entity (0.6) > remove entity (0.5) > multiple actions (each reduces by 0.05).
   */
  estimatedMinimalityScore: number;

  /**
   * Which violation pattern triggered this plan.
   * "undershoot" | "overshoot" | "window_failure" | "structural_failure"
   */
  violationType: "undershoot" | "overshoot" | "window_failure" | "structural_failure";
}

/* =========================================================
   Executor result
   ========================================================= */

/**
 * Result of applyGraphRepairPlan() — the new graph and a record of what changed.
 */
export interface GraphRepairExecutionResult {
  /** The modified graph (never mutates the input graph). */
  repairedGraph: import("../graph/canonical_graph_types.ts").CanonicalGraph;

  /** IDs of actions that were successfully applied. */
  appliedActionIds: string[];

  /** IDs of actions that were skipped (target not found, etc.). */
  skippedActionIds: string[];

  /**
   * Human-readable log of what was changed.
   * Example: "updated node cgn_me_0 quantity 40g→60g"
   */
  changeLog: string[];
}

/* =========================================================
   Validation result
   ========================================================= */

/**
 * Result of validateGraphRepairPlan() — whether the repair restored feasibility.
 */
export interface GraphRepairValidationResult {
  /** The plan ID that was validated. */
  repairPlanId: string;

  /** Always true — indicates that re-evaluation was actually performed. */
  reEvaluated: boolean;

  /** True if the originally failing constraint now passes in the repaired graph. */
  restoredFeasibility: boolean;

  /**
   * Constraint IDs that still fail in the repaired graph (excluding the target).
   * Empty if the repair resolved all violations.
   */
  remainingViolations: string[];

  /**
   * Constraint IDs that were passing before the repair but now fail in the repaired graph.
   * Empty if the repair introduced no new violations.
   */
  newViolations: string[];

  /**
   * Ordered human-readable summary.
   * Example:
   *   "Target constraint 'fast carbs before lifting': pass"
   *   "No new violations introduced"
   *   "Repair plan feasibility: restored"
   */
  summaryLines: string[];
}

/* =========================================================
   Planner input (constraint diff)
   ========================================================= */

/**
 * Everything buildGraphRepairPlan() needs to generate a repair plan.
 *
 * This aggregates the GraphNativeDiff with the constraint spec and proof trace
 * so the planner can detect violation type and select the right repair actions.
 */
export interface ConstraintDiffInput {
  /** The constraint spec that was violated. */
  spec: import("../graph/graph_constraint_types.ts").GraphConstraintSpec;

  /** The graph-native diff produced by the graph-first evaluator. */
  diff: import("../execution/execution_route_types.ts").GraphNativeDiff;

  /**
   * The graph-native proof trace from graph-first evaluation.
   * Used to extract per-step selected/excluded node IDs for violation type detection.
   */
  proof: import("../graph/graph_proof_types.ts").GraphConstraintProofTrace;
}
