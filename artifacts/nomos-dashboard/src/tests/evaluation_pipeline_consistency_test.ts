/**
 * evaluation_pipeline_consistency_test.ts
 *
 * Regression tests for the evaluation render pipeline.
 *
 * Enforces the invariant:
 *   If compiled constraints are all deterministic (no INTERPRETATION_REQUIRED),
 *   the mapped candidate cards must:
 *     - not contain any fallback reason text
 *     - not contain any stale "Clarify constraint semantics" adjustments
 *     - show a typed decisive variable (not "constraint interpretation")
 *     - be logically consistent with the footer note
 *
 * Also verifies:
 *   - The six NUTRITION_AUDIT template constraints are ALL deterministically
 *     classified by the dashboard compiler (no INTERPRETATION_REQUIRED).
 *   - The sanitization step strips residual fallback content from the API response.
 *   - The pipeline consistency check surfaces inconsistencies as errors.
 */

import { describe, it, expect, vi, beforeEach } from "vitest";
import {
  mapEvaluationResultToViewModel,
  mapCandidateEvaluation,
} from "../ui/mappers/evaluation_mapper";
import { compileConstraints } from "../compiler/constraint_compiler";
import { EvaluationResult, CandidateEvaluation } from "../ui/evaluation/eval_types";

/* =========================================================
   Shared fixtures
   ========================================================= */

/** The six constraints generated by the NUTRITION_AUDIT domain template. */
const NUTRITION_AUDIT_TEMPLATE_CONSTRAINTS = [
  "Preserve meal order unless explicitly allowed otherwise.",
  "Preserve meal count unless explicitly allowed otherwise.",
  "Preserve protein placement by meal unless explicitly allowed otherwise.",
  "Use attached food labels as source truth where provided.",
  "Do not infer food behavior that is not supported by declared labels or source data.",
  "If correction is requested, prefer the smallest structure-preserving change.",
];

/** A mock API evaluation result that contains stale LLM-fallback residue. */
function buildMockResultWithFallback(): EvaluationResult {
  const fallbackReason =
    "Constraint type could not be deterministically classified; evaluation requires manual review.";
  const typedReason = "Protein placement unchanged. Structural lock satisfied.";

  const candidateA: CandidateEvaluation = {
    id: "A",
    status: "LAWFUL",
    reason: `${typedReason} Additionally: ${fallbackReason}`,
    decisiveVariable: "constraint interpretation",
    adjustments: [
      "Clarify constraint semantics before re-evaluation.",
      "Maintain protein placement across all meals.",
    ],
    confidence: "moderate",
    marginScore: 0.72,
    marginLabel: "MODERATE",
  };

  const candidateB: CandidateEvaluation = {
    id: "B",
    status: "DEGRADED",
    reason: `${fallbackReason} Additionally: ${fallbackReason}`,
    decisiveVariable: "constraint interpretation",
    adjustments: [
      "Clarify constraint semantics before re-evaluation.",
      "Clarify constraint semantics before re-evaluation.",
    ],
    confidence: "low",
    marginScore: 0.41,
    marginLabel: "LOW",
  };

  return {
    overallStatus: "LAWFUL",
    lawfulSet: ["A"],
    candidateEvaluations: [candidateA, candidateB],
    decisiveVariable: "constraint interpretation",
    notes: ["6 constraint(s) evaluated against 2 candidate(s)."],
    bestCandidateId: "A",
    strongestMarginScore: 0.72,
    weakestAdmissibleMarginScore: 0.72,
  };
}

/* =========================================================
   Template constraint compilation
   ========================================================= */

describe("NUTRITION_AUDIT template constraints — all deterministic", () => {
  it("all 6 template constraints compile to non-INTERPRETATION_REQUIRED kinds", () => {
    const compiled = compileConstraints(NUTRITION_AUDIT_TEMPLATE_CONSTRAINTS);
    const fallbacks = compiled.filter((c) => c.kind === "INTERPRETATION_REQUIRED");
    expect(
      fallbacks.map((f) => f.raw),
      `These constraints still fall through to INTERPRETATION_REQUIRED: ${fallbacks.map((f) => f.raw).join("; ")}`
    ).toHaveLength(0);
  });

  it("compiles to the expected kinds", () => {
    const compiled = compileConstraints(NUTRITION_AUDIT_TEMPLATE_CONSTRAINTS);
    const kinds = compiled.map((c) => c.kind);
    expect(kinds).toContain("STRUCTURAL_LOCK");
    expect(kinds).toContain("SOURCE_TRUTH");
    expect(kinds).toContain("TARGET_TOLERANCE");
    expect(kinds).toContain("ALLOWED_ACTION");
    expect(kinds).not.toContain("INTERPRETATION_REQUIRED");
  });

  it("compiles protein placement to STRUCTURAL_LOCK:preserve_protein_placement", () => {
    const compiled = compileConstraints([
      "Preserve protein placement by meal unless explicitly allowed otherwise.",
    ]);
    expect(compiled[0]!.kind).toBe("STRUCTURAL_LOCK");
    expect(compiled[0]!.key).toBe("preserve_protein_placement");
    expect(compiled[0]!.decisiveVariable).toBe("protein placement");
  });

  it("compiles food-labels-as-source-truth to SOURCE_TRUTH:label_priority", () => {
    const compiled = compileConstraints([
      "Use attached food labels as source truth where provided.",
    ]);
    expect(compiled[0]!.kind).toBe("SOURCE_TRUTH");
    expect(compiled[0]!.key).toBe("label_priority");
    expect(compiled[0]!.operator).not.toBeNull();
  });

  it("compiles do-not-infer to ALLOWED_ACTION:inference_scope", () => {
    const compiled = compileConstraints([
      "Do not infer food behavior that is not supported by declared labels or source data.",
    ]);
    expect(compiled[0]!.kind).toBe("ALLOWED_ACTION");
    expect(compiled[0]!.key).toBe("inference_scope");
    expect(compiled[0]!.decisiveVariable).toBe("disallowed food inference");
    expect(compiled[0]!.operator).not.toBeNull();
  });

  it("compiles structure-preserving-change to TARGET_TOLERANCE:minimize_change_magnitude", () => {
    const compiled = compileConstraints([
      "If correction is requested, prefer the smallest structure-preserving change.",
    ]);
    expect(compiled[0]!.kind).toBe("TARGET_TOLERANCE");
    expect(compiled[0]!.key).toBe("minimize_change_magnitude");
  });
});

/* =========================================================
   Sanitization: stale fallback text must be stripped
   ========================================================= */

describe("mapEvaluationResultToViewModel — sanitization with all-deterministic constraints", () => {
  const compiledConstraints = compileConstraints(NUTRITION_AUDIT_TEMPLATE_CONSTRAINTS);

  it("all compiled constraints are deterministic (precondition for sanitization)", () => {
    const fallbacks = compiledConstraints.filter((c) => c.kind === "INTERPRETATION_REQUIRED");
    expect(fallbacks).toHaveLength(0);
  });

  it("candidate A reason does not contain fallback text after mapping", () => {
    const vm = mapEvaluationResultToViewModel(
      buildMockResultWithFallback(),
      compiledConstraints
    );
    const cardA = vm.candidateCards.find((c) => c.id === "A")!;
    expect(cardA.reason).not.toMatch(/could not be deterministically classified/i);
    expect(cardA.reason).not.toMatch(/evaluation requires manual review/i);
    expect(cardA.reason).not.toMatch(/constraint interpretation/i);
  });

  it("candidate A reason retains the typed clause", () => {
    const vm = mapEvaluationResultToViewModel(
      buildMockResultWithFallback(),
      compiledConstraints
    );
    const cardA = vm.candidateCards.find((c) => c.id === "A")!;
    expect(cardA.reason).toMatch(/protein placement/i);
  });

  it("candidate B reason does not contain fallback text after mapping", () => {
    const vm = mapEvaluationResultToViewModel(
      buildMockResultWithFallback(),
      compiledConstraints
    );
    const cardB = vm.candidateCards.find((c) => c.id === "B")!;
    expect(cardB.reason).not.toMatch(/could not be deterministically classified/i);
    expect(cardB.reason).not.toMatch(/evaluation requires manual review/i);
  });

  it("candidate B reason is replaced with deterministic fallback message when all clauses were stale", () => {
    const vm = mapEvaluationResultToViewModel(
      buildMockResultWithFallback(),
      compiledConstraints
    );
    const cardB = vm.candidateCards.find((c) => c.id === "B")!;
    expect(cardB.reason.length).toBeGreaterThan(0);
    expect(cardB.reason).not.toMatch(/Constraint type could not/i);
  });

  it("adjustments do not contain 'Clarify constraint semantics' after mapping", () => {
    const vm = mapEvaluationResultToViewModel(
      buildMockResultWithFallback(),
      compiledConstraints
    );
    for (const card of vm.candidateCards) {
      for (const adj of card.adjustments) {
        expect(adj.toLowerCase()).not.toContain("clarify constraint semantics");
      }
    }
  });

  it("decisive variable is not 'constraint interpretation' for any candidate", () => {
    const vm = mapEvaluationResultToViewModel(
      buildMockResultWithFallback(),
      compiledConstraints
    );
    for (const card of vm.candidateCards) {
      expect(card.decisiveVariable?.toLowerCase()).not.toContain("constraint interpretation");
    }
  });

  it("global decisive variable is not 'constraint interpretation'", () => {
    const vm = mapEvaluationResultToViewModel(
      buildMockResultWithFallback(),
      compiledConstraints
    );
    expect(vm.decisiveVariable?.toLowerCase()).not.toContain("constraint interpretation");
  });

  it("classification note says 'evaluated deterministically' (not 'passed')", () => {
    const vm = mapEvaluationResultToViewModel(
      buildMockResultWithFallback(),
      compiledConstraints
    );
    const classificationNote = vm.notes.find((n) =>
      n.toLowerCase().includes("evaluated deterministically")
    );
    expect(classificationNote).toBeDefined();
    expect(classificationNote!.toLowerCase()).not.toContain("passed");
  });

  it("notes include a separate satisfaction verdict line", () => {
    const vm = mapEvaluationResultToViewModel(
      buildMockResultWithFallback(),
      compiledConstraints
    );
    const satisfactionNote = vm.notes.find(
      (n) =>
        n.toLowerCase().includes("constraint-admissible") ||
        n.toLowerCase().includes("violations detected")
    );
    expect(satisfactionNote).toBeDefined();
  });

  it("classification note and candidate cards are logically consistent", () => {
    const vm = mapEvaluationResultToViewModel(
      buildMockResultWithFallback(),
      compiledConstraints
    );
    const noteClaimsDeterministic = vm.notes.some((n) =>
      n.toLowerCase().includes("evaluated deterministically")
    );
    if (noteClaimsDeterministic) {
      for (const card of vm.candidateCards) {
        expect(card.reason).not.toMatch(/could not be deterministically classified/i);
        expect(card.reason).not.toMatch(/evaluation requires manual review/i);
        for (const adj of card.adjustments) {
          expect(adj.toLowerCase()).not.toContain("clarify constraint semantics");
        }
      }
    }
  });
});

/* =========================================================
   mapCandidateEvaluation — unit tests for sanitization
   ========================================================= */

describe("mapCandidateEvaluation — fallback stripping", () => {
  const allDeterministicConstraints = compileConstraints(NUTRITION_AUDIT_TEMPLATE_CONSTRAINTS);

  it("strips fallback reason clause when allDeterministic=true", () => {
    const evaluation: CandidateEvaluation = {
      id: "X",
      status: "LAWFUL",
      reason:
        "Protein placement unchanged. Structural lock satisfied. Additionally: Constraint type could not be deterministically classified; evaluation requires manual review.",
      decisiveVariable: "protein placement",
      adjustments: [],
      confidence: "high",
      marginScore: 0.9,
      marginLabel: "HIGH",
    };

    const card = mapCandidateEvaluation(evaluation, allDeterministicConstraints, true);
    expect(card.reason).not.toMatch(/could not be deterministically classified/i);
    expect(card.reason).toMatch(/protein placement/i);
  });

  it("strips 'Clarify constraint semantics' adjustment when allDeterministic=true", () => {
    const evaluation: CandidateEvaluation = {
      id: "X",
      status: "LAWFUL",
      reason: "Protein placement unchanged. Structural lock satisfied.",
      decisiveVariable: "protein placement",
      adjustments: [
        "Clarify constraint semantics before re-evaluation.",
        "Maintain protein placement across all meals.",
      ],
      confidence: "high",
      marginScore: 0.9,
      marginLabel: "HIGH",
    };

    const card = mapCandidateEvaluation(evaluation, allDeterministicConstraints, true);
    expect(card.adjustments).not.toContain("Clarify constraint semantics before re-evaluation.");
    expect(card.adjustments.some((a) => a.toLowerCase().includes("protein placement"))).toBe(true);
  });

  it("does NOT strip fallback clauses when allDeterministic=false", () => {
    const evaluation: CandidateEvaluation = {
      id: "X",
      status: "DEGRADED",
      reason:
        "Constraint type could not be deterministically classified; evaluation requires manual review.",
      decisiveVariable: "constraint interpretation",
      adjustments: ["Clarify constraint semantics before re-evaluation."],
      confidence: "low",
      marginScore: 0.3,
      marginLabel: "LOW",
    };

    const card = mapCandidateEvaluation(evaluation, undefined, false);
    expect(card.reason).not.toBe("");
    expect(card.reason.length).toBeGreaterThan(0);
  });

  it("deduplicates repeated identical reason clauses", () => {
    const evaluation: CandidateEvaluation = {
      id: "X",
      status: "LAWFUL",
      reason:
        "Protein placement unchanged. Structural lock satisfied. Additionally: Protein placement unchanged. Structural lock satisfied.",
      decisiveVariable: "protein placement",
      adjustments: [],
      confidence: "high",
      marginScore: 0.9,
      marginLabel: "HIGH",
    };

    const card = mapCandidateEvaluation(evaluation, allDeterministicConstraints, true);
    const parts = card.reason.split(" Additionally: ");
    expect(parts.length).toBe(1);
  });

  it("deduplicates repeated adjustments", () => {
    const evaluation: CandidateEvaluation = {
      id: "X",
      status: "DEGRADED",
      reason: "Timing adjustment detected.",
      decisiveVariable: "meal dispersal",
      adjustments: [
        "Confirm timing changes do not alter dispersal pattern.",
        "Confirm timing changes do not alter dispersal pattern.",
        "Confirm timing changes do not alter dispersal pattern.",
      ],
      confidence: "moderate",
      marginScore: 0.55,
      marginLabel: "MODERATE",
    };

    const card = mapCandidateEvaluation(evaluation, allDeterministicConstraints, false);
    const count = card.adjustments.filter((a) =>
      a.toLowerCase().includes("confirm timing")
    ).length;
    expect(count).toBe(1);
  });
});

/* =========================================================
   Regression: no INTERPRETATION_REQUIRED for known template constraints
   ========================================================= */

describe("regression: template constraints never produce INTERPRETATION_REQUIRED", () => {
  it("is idempotent — compiling template constraints twice gives same result", () => {
    const first = compileConstraints(NUTRITION_AUDIT_TEMPLATE_CONSTRAINTS);
    const second = compileConstraints(NUTRITION_AUDIT_TEMPLATE_CONSTRAINTS);
    expect(second).toStrictEqual(first);
  });

  it("all operators are non-null — all deterministic paths have an operator", () => {
    const compiled = compileConstraints(NUTRITION_AUDIT_TEMPLATE_CONSTRAINTS);
    for (const c of compiled) {
      expect(c.operator, `constraint "${c.raw}" has null operator`).not.toBeNull();
    }
  });

  it("all lhs and rhs are non-null", () => {
    const compiled = compileConstraints(NUTRITION_AUDIT_TEMPLATE_CONSTRAINTS);
    for (const c of compiled) {
      expect(c.lhs, `constraint "${c.raw}" has null lhs`).not.toBeNull();
      expect(c.rhs, `constraint "${c.raw}" has null rhs`).not.toBeNull();
    }
  });
});

/* =========================================================
   Regression: classification ≠ satisfaction
   The UI must distinguish between HOW a constraint was evaluated
   (deterministic vs interpretation-required) and WHETHER the
   candidate satisfied it (LAWFUL vs DEGRADED/INVALID).

   The previous bug: "All constraints passed deterministic evaluation."
   conflated these two concepts. These tests enforce they stay separate.
   ========================================================= */

describe("regression: classification vs satisfaction — must never be conflated", () => {
  const compiledConstraints = compileConstraints(NUTRITION_AUDIT_TEMPLATE_CONSTRAINTS);

  /** A result where evaluation was deterministic but a violation was found. */
  function buildDeterministicViolationResult(): EvaluationResult {
    return {
      overallStatus: "DEGRADED",
      lawfulSet: ["A"],
      candidateEvaluations: [
        {
          id: "A",
          status: "LAWFUL",
          reason: "Protein placement unchanged. Structural lock satisfied.",
          decisiveVariable: "protein placement",
          adjustments: [],
          confidence: "high",
          marginScore: 0.85,
          marginLabel: "HIGH",
        },
        {
          id: "B",
          status: "DEGRADED",
          reason: "Protein placement moved between meals. Structural lock violated.",
          decisiveVariable: "protein placement",
          adjustments: ["Restore protein to its original meal placement."],
          confidence: "high",
          marginScore: 0.35,
          marginLabel: "LOW",
        },
        {
          id: "C",
          status: "DEGRADED",
          reason: "Meal order altered. Structural lock violated.",
          decisiveVariable: "meal order",
          adjustments: ["Restore the original meal sequence."],
          confidence: "high",
          marginScore: 0.28,
          marginLabel: "LOW",
        },
      ],
      decisiveVariable: "protein placement",
      notes: ["6 constraint(s) evaluated against 3 candidate(s)."],
      bestCandidateId: "A",
      strongestMarginScore: 0.85,
      weakestAdmissibleMarginScore: 0.85,
    };
  }

  it("notes never contain 'passed' when there is a violation", () => {
    const vm = mapEvaluationResultToViewModel(
      buildDeterministicViolationResult(),
      compiledConstraints
    );
    for (const note of vm.notes) {
      expect(
        note.toLowerCase(),
        `Note should not say "passed" when violations exist: "${note}"`
      ).not.toContain("passed");
    }
  });

  it("notes contain 'evaluated deterministically' (classification) when all compiled", () => {
    const vm = mapEvaluationResultToViewModel(
      buildDeterministicViolationResult(),
      compiledConstraints
    );
    const classNote = vm.notes.find((n) => n.toLowerCase().includes("evaluated deterministically"));
    expect(classNote).toBeDefined();
  });

  it("notes contain a satisfaction verdict that mentions violations", () => {
    const vm = mapEvaluationResultToViewModel(
      buildDeterministicViolationResult(),
      compiledConstraints
    );
    const satisfactionNote = vm.notes.find(
      (n) =>
        n.toLowerCase().includes("violations") ||
        n.toLowerCase().includes("constraint-admissible")
    );
    expect(satisfactionNote).toBeDefined();
  });

  it("classification note and satisfaction note are separate strings", () => {
    const vm = mapEvaluationResultToViewModel(
      buildDeterministicViolationResult(),
      compiledConstraints
    );
    const classNote = vm.notes.find((n) => n.toLowerCase().includes("evaluated deterministically"));
    const satNote = vm.notes.find(
      (n) =>
        n.toLowerCase().includes("violations") ||
        n.toLowerCase().includes("constraint-admissible")
    );
    expect(classNote).not.toBe(satNote);
    expect(classNote).toBeDefined();
    expect(satNote).toBeDefined();
  });

  it("satisfaction note correctly counts admissible vs violated candidates", () => {
    const vm = mapEvaluationResultToViewModel(
      buildDeterministicViolationResult(),
      compiledConstraints
    );
    // 1 LAWFUL, 2 DEGRADED → "1 of 3 candidates are constraint-admissible; 2 have violations."
    const satNote = vm.notes.find((n) => n.toLowerCase().includes("constraint-admissible"));
    expect(satNote).toBeDefined();
    expect(satNote).toMatch(/1 of 3/);
    expect(satNote).toMatch(/2 have violations/);
  });

  it("when all candidates are violated, note says violations detected across all", () => {
    const allViolated: EvaluationResult = {
      overallStatus: "DEGRADED",
      lawfulSet: [],
      candidateEvaluations: [
        {
          id: "A",
          status: "DEGRADED",
          reason: "Protein placement moved.",
          decisiveVariable: "protein placement",
          adjustments: [],
          confidence: "high",
          marginScore: 0.2,
          marginLabel: "LOW",
        },
        {
          id: "B",
          status: "DEGRADED",
          reason: "Meal order altered.",
          decisiveVariable: "meal order",
          adjustments: [],
          confidence: "high",
          marginScore: 0.15,
          marginLabel: "LOW",
        },
      ],
      decisiveVariable: "protein placement",
      notes: [],
      bestCandidateId: undefined,
      strongestMarginScore: 0.2,
      weakestAdmissibleMarginScore: undefined,
    };

    const vm = mapEvaluationResultToViewModel(allViolated, compiledConstraints);
    const satNote = vm.notes.find((n) =>
      n.toLowerCase().includes("violations detected across all")
    );
    expect(satNote).toBeDefined();
    expect(satNote!.toLowerCase()).not.toContain("passed");
  });

  it("when all candidates are admissible, note says all are constraint-admissible", () => {
    const allLawful: EvaluationResult = {
      overallStatus: "LAWFUL",
      lawfulSet: ["A", "B"],
      candidateEvaluations: [
        {
          id: "A",
          status: "LAWFUL",
          reason: "All structural locks satisfied.",
          decisiveVariable: "protein placement",
          adjustments: [],
          confidence: "high",
          marginScore: 0.92,
          marginLabel: "HIGH",
        },
        {
          id: "B",
          status: "LAWFUL",
          reason: "All structural locks satisfied.",
          decisiveVariable: "protein placement",
          adjustments: [],
          confidence: "high",
          marginScore: 0.87,
          marginLabel: "HIGH",
        },
      ],
      decisiveVariable: "protein placement",
      notes: [],
      bestCandidateId: "A",
      strongestMarginScore: 0.92,
      weakestAdmissibleMarginScore: 0.87,
    };

    const vm = mapEvaluationResultToViewModel(allLawful, compiledConstraints);
    const satNote = vm.notes.find((n) =>
      n.toLowerCase().includes("all") && n.toLowerCase().includes("constraint-admissible")
    );
    expect(satNote).toBeDefined();
    expect(satNote!.toLowerCase()).not.toContain("violation");
  });

  it("'protein placement violation' as decisive variable never produces a 'passed' note", () => {
    const vm = mapEvaluationResultToViewModel(
      buildDeterministicViolationResult(),
      compiledConstraints
    );
    expect(vm.decisiveVariable).toMatch(/protein placement violation/i);
    const badNote = vm.notes.find((n) => n.toLowerCase().includes("passed"));
    expect(badNote).toBeUndefined();
  });
});
